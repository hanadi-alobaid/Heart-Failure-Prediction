import pandas as pd
import numpy as np
from pandas_profiling import ProfileReport
from sklearn.preprocessing import MinMaxScaler
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import f1_score

from google.colab import drive
drive.mount('/content/drive', force_remount=True)

Data_Path='/My_Path/heart_failure_clinical_records_dataset.csv'

data = pd.read_csv(Data_Path)
data

"""##Pandas Profiling Report For The Dataset"""

profile=ProfileReport(data)

profile.to_notebook_iframe()

"""## Scaling Dataset"""

X=data.drop('DEATH_EVENT',axis=1)
Y=data['DEATH_EVENT']

scaler = MinMaxScaler()
X = scaler.fit_transform(X)

X=pd.DataFrame(X)
X

"""###Modeling"""

x_train, x_test, y_train, y_test = train_test_split(X, Y, test_size=0.8, random_state=0)

"""###Init model"""

model = LogisticRegression()

"""###Train model"""

model.fit(x_train, y_train)

"""###Test model"""

y_pred = model.predict(x_test)

"""###Evaluate model"""

Accuracy = model.score(x_test, y_test)

"""### Evaluate how good the logistic model performed"""

Score = f1_score(y_test, y_pred)

"""### Output :"""

print(f"Logistic Regression:\nAccuracy: {Accuracy}\nF1 Score: {Score}\n")